//Vamos a crear una clase Calculadora con sus methods sumar restar multiplicar dividir 

class Calculadora{
    sumar(numero1,numero2){//parametro pedimos dos numeros 
        return numero1 + numero2;//retornamos el resultado con return
    }

    restar(numero1,numero2){//parametro pedimos dos numeros 
        return numero1 - numero2;//retornamos el resultado con return
    }

    multiplicar(numero1,numero2){//parametro pedimos dos numeros 
        return numero1 * numero2;//retornamos el resultado con return
    }

    dividir(numero1,numero2){//parametro pedimos dos numeros 
        return numero1 / numero2;//retornamos el resultado con return
    }
}